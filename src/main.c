#include "headers/decoder.h"
#include "headers/dump.h"
#include <stdio.h>

#define INSTRUCTION_LIMIT 100 

int main(void) {
  // todo: add valid prefix check
  // ex: 66 0f 74 04 00 -> is a valid instruction, while f3 0f 74 04 00 is not
  uint64_t offset, instructions;
  uchar8_t instruction[] = {
      0xF3, 0x0F, 0x1E, 0xFB, 0xE9, 0x57, 0xFF, 0xFF, 0xFF, 0x8B, 0x14,
      0x24, 0xC3, 0x8D, 0x4C, 0x24, 0x04, 0x83, 0xE4, 0xF0, 0xFF, 0x71,
      0xFC, 0x55, 0x89, 0xE5, 0x53, 0x51, 0xE8, 0x28, 0x00, 0x00, 0x00,
      0x66, 0x05, 0x53, 0x2E, 0x00, 0x00, 0x83, 0xEC, 0x0C, 0x8D, 0x90,
      0x14, 0xE0, 0xFF, 0xFF, 0x52, 0x89, 0xC3, 0xE8, 0x89, 0xFE, 0xFF,
      0xFF, 0x83, 0xC4, 0x10, 0xB8, 0x00, 0x00, 0x00, 0x00, 0x8D, 0x65,
      0xF8, 0x59, 0x5B, 0x5D, 0x8D, 0x61, 0xFC, 0xC3, 0x0f, 0x18, 0x00};
  Dinstruction *decoded = pedix_init_instruction();
  decoded->mode = MODE_32;

  /* curr offset */
  offset = 0;
  /* instruction counter */
  instructions = 0;

  while (instructions < INSTRUCTION_LIMIT) {
   pedix_decode(decoded, instruction + offset);

#ifdef DEBUG
    pedix_dump_instruction(decoded->instruction);
#endif
    /* if everything went well */
    pedix_print_instruction(decoded, offset);
    /* eat bytes */
    offset += decoded->buffer.size;
    /* go to the next instruction */
    instructions++;
    /* zero the struct */
    pedix_zero_instruction(decoded);
  }

  pedix_free_instrucion(decoded);
  return 0;
}

