#include <pedix/decoder.h>
#include <pedix/dump.h>
#include <stdio.h>

//#define INSTRUCTION_LIMIT 102 
#define INSTRUCTION_LIMIT 28 


int main(void) {
  // todo: add valid prefix check
  // ex: 66 0f 74 04 00 -> is a valid instruction, while f3 0f 74 04 00 is not
  uint64_t offset, instructions;
//  uint8_t instruction[] = { 0xD9, 0x28};
  uint8_t instruction[] = {
      0x8D, 0x4C, 0x24, 0x04, 0x83, 0xE4, 0xF0, 0xFF, 0x71, 0xFC, 0x55,
      0x89, 0xE5, 0x53, 0x51, 0xE8, 0xB8, 0xFD, 0xFF, 0xFF, 0x81, 0xC3,
      0xCC, 0x2C, 0x00, 0x00, 0x83, 0xEC, 0x0C, 0x8D, 0x83, 0x94, 0xE0,
      0xFF, 0xFF, 0x50, 0xE8, 0x23, 0xFD, 0xFF, 0xFF, 0x83, 0xC4, 0x10,
      0xE8, 0x79, 0xFF, 0xFF, 0xFF, 0xE8, 0x93, 0xFE, 0xFF, 0xFF, 0x83,
      0xEC, 0x0C, 0x8D, 0x83, 0xBC, 0xE0, 0xFF, 0xFF, 0x50, 0xE8, 0x07,
      0xFD, 0xFF, 0xFF, 0x83, 0xC4, 0x10, 0xB8, 0x00, 0x00, 0x00, 0x00,
      0x8D, 0x65, 0xF8, 0x59, 0x5B, 0x5D, 0x8D, 0x61, 0xFC, 0xC3};
  decoded_instruction_t *decoded = pedix_init_instruction();
  pedix_set_mode(decoded, MODE_32);

  /* curr offset */
  offset = 0;
  /* instruction counter */
  instructions = 0;

  while (instructions < INSTRUCTION_LIMIT) {
   pedix_decode(decoded, instruction + offset);

#ifdef DEBUG
    pedix_dump_instruction(decoded->instruction);
#endif
    /* if everything went well */
    pedix_print_instruction(decoded, offset);
    /* eat bytes */
    offset += decoded->buffer.size;
    /* go to the next instruction */
    instructions++;
    /* zero the struct */
    pedix_zero_instruction(decoded);
  }

  pedix_free_instrucion(decoded);
  return 0;
}

